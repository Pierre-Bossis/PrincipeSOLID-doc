//Liskov substitution principle

//si une méthode attend une classe de base, et qu'on lui passe une classe plus spécifique ou qui a un héritage,le comportement de la méthode
//ne doit pas être cassé parce qu'on lui passe une classe plus spécifique.

//LSP - Principe de substitution de Liskov :
//Les sous-classes doivent pouvoir remplacer leur classe parente sans altérer le comportement du programme.
//Par exemple, un rectangle devrait pouvoir remplacer une forme sans problème.